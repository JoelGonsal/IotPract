#Derive the expressions based on Associative law
import random 
a=random.randint(0,99) 
b=random.randint(0,99) 
c=random.randint(0,99) 
print("Associative Law : "); 
print("A+(B+C)->",(a+(b+c))) 
print("(A+B)+C->",((a+b)+c)) 
